    def blue(self):
        return (0, 0, 255)
    def yellow(self):
        return (255, 255, 0)
    def white(self):
        return(255, 255, 255)
    def orange(self):
        return(255, 165, 0)
    def lightblue(self):
        return(173, 216, 230)
    def gold(self):
        return(255, 215, 0)
